def add_column_comments(engine):
    """
    Add documentation comments to all columns (PostgreSQL only).
    This ensures comments are set even if SQLAlchemy didn't apply them during table creation.
    """
    # Collect all column comments from the models
    column_comments = {}
    for mapper in Base.registry.mappers:
        table_name = mapper.local_table.name
        column_comments[table_name] = {}
        for column in mapper.local_table.columns:
            if column.comment:
                column_comments[table_name][column.name] = column.comment

    with engine.connect() as conn:
        for table_name, columns in column_comments.items():
            for column_name, comment in columns.items():
                try:
                    conn.execute(
                        text(f'COMMENT ON COLUMN {table_name}.{column_name} IS :comment'),
                        {"comment": comment}
                    )
                except Exception:
                    pass  # SQLite doesn't support COMMENT
        conn.commit()


