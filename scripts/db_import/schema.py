"""
BeFaaS Benchmark Results Database Schema
=========================================

This database stores results from load testing experiments comparing different cloud
architectures (FaaS/Lambda, Microservices/ECS, Monolith/ECS) with different authentication
strategies (none, service-integrated, edge).

DATA MODEL OVERVIEW
-------------------

The data flows from a load test experiment as follows:

1. Artillery (load generator) sends HTTP requests to the application
2. The application runs on AWS (Lambda or ECS Fargate)
3. BEFAAS instrumentation logs server-side events
4. CloudWatch captures platform metrics
5. After the experiment, all data is collected and imported here

KEY CONCEPTS
------------

- **Experiment**: A single benchmark run with a specific architecture + auth strategy + hardware config
- **Architecture**: 'faas' (AWS Lambda), 'microservices' (ECS with multiple services), 'monolith' (ECS single service)
- **Auth Strategy**: 'none' (no auth), 'service-integrated' (JWT validation in each handler),
  'service-integrated-manual' (manual JWT), 'edge' (auth at API Gateway/ALB level)
- **x_pair**: Unique request ID generated by Artillery, used to correlate client requests with server events
- **Phase**: Benchmark phases like 'warmup', 'baseline', 'stress' with different load patterns

TABLE RELATIONSHIPS
-------------------

experiments (1) ──┬── (N) requests           # Client-side HTTP requests
                  ├── (N) handler_events     # Server-side handler executions
                  ├── (N) lambda_executions  # Lambda platform metrics (FaaS only)
                  ├── (N) container_starts   # Cold start events
                  ├── (N) rpc_calls          # Inter-service calls
                  ├── (N) phases             # Benchmark phase definitions
                  ├── (N) scaling_rules      # ECS auto-scaling config
                  ├── (1) pricing            # Aggregated cost data
                  ├── (N) pricing_components # Per-function/service costs
                  ├── (N) metrics_ecs        # ECS time-series metrics
                  ├── (N) metrics_alb        # ALB time-series metrics
                  └── (N) edge_auth_events   # Lambda@Edge BEFAAS-EDGE events (edge/edge-selective v2 only)

CORRELATION KEYS
----------------

To trace a request end-to-end:
- requests.x_pair = handler_events.x_pair = rpc_calls.x_pair
- handler_events.lambda_request_id = lambda_executions.request_id = container_starts.lambda_request_id

COMMON QUERIES
--------------

-- Compare latency across architectures:
SELECT architecture, auth_strategy,
       percentile_cont(0.5) WITHIN GROUP (ORDER BY latency_ms) as p50,
       percentile_cont(0.95) WITHIN GROUP (ORDER BY latency_ms) as p95
FROM requests r JOIN experiments e ON r.experiment_id = e.id
WHERE phase_name = 'baseline' AND NOT is_error
GROUP BY architecture, auth_strategy;

-- Cold start analysis for Lambda:
SELECT function_name, COUNT(*) as cold_starts, AVG(init_duration_ms) as avg_init_ms
FROM lambda_executions WHERE is_cold_start AND experiment_id = ?
GROUP BY function_name;

-- Auth overhead (compare handler duration with/without auth):
SELECT e.auth_strategy, AVG(h.duration_ms) as avg_handler_ms
FROM handler_events h JOIN experiments e ON h.experiment_id = e.id
WHERE h.is_protected_endpoint AND phase_name = 'baseline'
GROUP BY e.auth_strategy;
"""

